
package day.pkg41;


public class Day41 {

    
    public static void main(String[] args) {
        //Operator || pada kondisi if
        
        int angka1 = 5;
        int angka2 = 10;
        
        if (angka1 >= 0 || angka2 <= 5){
            System.out.println("Salah satu kondisi terpenuhi");
        }else{
            System.out.println("Kedua kondisi tidak terpenuhi");
            
        }
        
    }
    
}
