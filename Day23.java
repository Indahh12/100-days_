package day.pkg23;

public class Day23 {

    
    public static void main(String[] args) {
        int angka1= 5;
        int angka2 = 10;
        
        //Operator pembanding '=='
        boolean sama = angka1 == angka2;
        System.out.println("Apakah angka1 sama dengan angka2? = " + sama);
        
        //Operator pembanding '!='
        boolean tidakSama = angka1 != angka2;
        System.out.println("Apakah angka1 tidak sama dengan angka 2? = " + tidakSama);
        
        //Operator pembanding '>'
        boolean lebihBesar = angka1 > angka2;
        System.out.println("Apakah angka1 lebih besar dari angka2 ? = " + lebihBesar);
        
        //Operator prmbandingan '<'
        boolean lebihKecil = angka1 < angka2 ;
        System.out.println("Apakah angka1 lebih kecil dari angka2? = " + lebihKecil);
        
        //Operator pembanding '>='
        boolean lebihBesarSamaDengan = angka1 >= angka2;
        System.out.println("Apakah angka1 lebih besar  atau sama dengan angka 2? = " + lebihBesarSamaDengan);
        
        //Operator pembanding '<='
        boolean lebihKecilSamaDengan = angka1 <= angka2;
        System.out.println("Apakah angka1 lebin kecil atau sama dengan angka2? = " + lebihKecilSamaDengan);
        
        
        
                
                
        
        
    }
    
}
